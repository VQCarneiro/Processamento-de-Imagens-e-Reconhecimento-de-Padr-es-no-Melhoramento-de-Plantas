########################################################################################################################
# Importar pacotes
import cv2 # Importa o pacote opencv
import numpy as np # Importa o pacote numpy
from matplotlib import pyplot as plt # Importa o pacote matplotlib
########################################################################################################################
# Manipulação de pixels em imagens
########################################################################################################################
# Leitura da imagem

nome_arquivo = 'img_quadrados_cores.png' # Nome do arquivo a ser utilizado na análise
img_bgr = cv2.imread(nome_arquivo, 1) # Carrega imagem colorida ou em escala de cinza
img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)

########################################################################################################################
# Modificação na imagem
img_mod_02=np.copy(img_rgb)
img_mod_02[1:4,1:4,0] = 255
img_mod_02[7:10,7:10,1] = 255
img_mod_02[4:7,4:7,2] = 255

print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZES DA IMAGEM MODIFICADA NO ESPAÇO DE COR RGB')
print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZ R ORIGINAL')
print('----------------------------------------------------------------------------------------------------------------')
print(img_rgb[:,:,0])
print('----------------------------------------------------------------------------------------------------------------')
print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZ R MODIFICADA img_mod_02[1:4,1:4,0] = 255')
print('----------------------------------------------------------------------------------------------------------------')
print(img_mod_02[:,:,0])
print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZ G ORIGINAL')
print('----------------------------------------------------------------------------------------------------------------')
print(img_rgb[:,:,1])
print('----------------------------------------------------------------------------------------------------------------')
print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZ G MODIFICADA img_mod_02[7:10,7:10,1] = 255')
print('----------------------------------------------------------------------------------------------------------------')
print(img_mod_02[:,:,1])
print('----------------------------------------------------------------------------------------------------------------')
print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZ B ORIGINAL')
print('----------------------------------------------------------------------------------------------------------------')
print(img_rgb[:,:,2])
print('----------------------------------------------------------------------------------------------------------------')
print('MATRIZ B MODIFICADA img_mod_02[4:7,4:7,2] = 255')
print('----------------------------------------------------------------------------------------------------------------')
print(img_mod_02[:,:,2])
print('----------------------------------------------------------------------------------------------------------------')
########################################################################################################################

# Apresentação das imagens
plt.figure("Imagens")
plt.subplot(1,2,1)
plt.imshow(img_rgb)
plt.title("ORIGINAL")

plt.subplot(1,2,2)
plt.imshow(img_mod_02)
plt.title("MOD 02")
plt.show()
